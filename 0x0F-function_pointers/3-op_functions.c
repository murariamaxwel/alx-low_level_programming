/**
 * op_add - carries out addition
 * @a: one of the integers to compute
 * @b: the other integer
 * Return: result of computation
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - carries out substraction
 * @a: one of the integers to compute
 * @b: the other integer
 * Return: result of substraction
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplication
 * @a: one integer
 * @b: the other integer
 * Return: multiplication result
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - carry out division
 * @a: one int
 * @b: the other int
 * Return: division computation result
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulus operation
 * @a: one integer
 * @b: the other integer
 * Return: result of modulus computation
 */
int op_mod(int a, int b)
{
	return (a % b);
}
